// Global translation helpers
export function translateWilaya(wilaya: string, lang: 'fr' | 'ar'): string {
  if (!wilaya) return wilaya;
  const cleaned = wilaya.trim().toLowerCase();
  
  if (lang === 'fr') {
    // Normalisations françaises pour accents et majuscules
    const FR_NORMALIZED: Record<string, string> = {
      "sidi bel abbes": "Sidi Bel Abbès",
      "sidi bel abbès": "Sidi Bel Abbès",
      "el tarf": "El Tarf",
      "khenchela": "Khenchela",
      "el m'ghair": "El M'Ghair",
    };
    return FR_NORMALIZED[cleaned] || wilaya;
  }
  
  return WILAYAS_AR[cleaned] || wilaya;
}

export function translateSpecialtyName(name: string, lang: 'fr' | 'ar', id?: string): string {
  if (lang === 'fr' || !name) return name;

  // Try matching via custom ID if provided
  if (id && SPECIALTIES_TRANSLATIONS[id]) {
    return SPECIALTIES_TRANSLATIONS[id].name;
  }

  // Fallback search in translation keys
  const keys = Object.keys(SPECIALTIES_TRANSLATIONS);
  for (const k of keys) {
    const item = SPECIALTIES_TRANSLATIONS[k];
    if (name.toUpperCase().includes(k.toUpperCase()) || k.toUpperCase().includes(name.toUpperCase())) {
      return item.name;
    }
  }

  // Specific mappings for abbreviations or alternative spellings
  const nameUpper = name.toUpperCase();
  if (nameUpper.includes("SAGE FEMME") || nameUpper.includes("SAGE-FEMME")) return SPECIALTIES_TRANSLATIONS["sage-femme"].name;
  if (nameUpper.includes("INFIRMIER") || nameUpper.includes("INFIMIERS") || nameUpper.includes("ISP")) return SPECIALTIES_TRANSLATIONS["isp"].name;
  if (nameUpper.includes("DIET") || nameUpper.includes("DIETETICIEN")) return SPECIALTIES_TRANSLATIONS["diet"].name;
  if (nameUpper.includes("PROTH") || nameUpper.includes("PROTHESISTE")) return SPECIALTIES_TRANSLATIONS["prothese"].name;
  if (nameUpper.includes("KINE") || nameUpper.includes("KINESITHERAPEUTE")) return SPECIALTIES_TRANSLATIONS["kine"].name;
  if (nameUpper.includes("PODO") || nameUpper.includes("PEDICURE")) return SPECIALTIES_TRANSLATIONS["podo"].name;
  if (nameUpper.includes("ERGO") || nameUpper.includes("ERGOTHERAPEUTE")) return SPECIALTIES_TRANSLATIONS["ergo"].name;
  if (nameUpper.includes("MANIP") || nameUpper.includes("RADIO") || nameUpper.includes("RADIOLOGIE")) return SPECIALTIES_TRANSLATIONS["manip-radio"].name;
  if (nameUpper.includes("LABORANTIN")) return SPECIALTIES_TRANSLATIONS["laborantin"].name;
  if (nameUpper.includes("ASSISTANT")) return SPECIALTIES_TRANSLATIONS["assistant-med"].name;
  if (nameUpper.includes("OPTIQUE") || nameUpper.includes("OPTICIEN")) return SPECIALTIES_TRANSLATIONS["optique"].name;
  if (nameUpper.includes("ORTHOPHONIE") || nameUpper.includes("ORTHOPHONISTE")) return SPECIALTIES_TRANSLATIONS["orthophonie"].name;
  if (nameUpper.includes("PSYCHOLOGIE") || nameUpper.includes("PSYCHOLOGUE")) return SPECIALTIES_TRANSLATIONS["psychologie"].name;

  return name;
}

export function translateSpecialtyDesc(desc: string, lang: 'fr' | 'ar', id?: string): string {
  if (lang === 'fr' || !desc) return desc;
  if (id && SPECIALTIES_TRANSLATIONS[id]) return SPECIALTIES_TRANSLATIONS[id].desc;

  const keys = Object.keys(SPECIALTIES_TRANSLATIONS);
  for (const k of keys) {
    if (desc.toLowerCase().includes(k)) {
      return SPECIALTIES_TRANSLATIONS[k].desc;
    }
  }
  return desc;
}

export function translateSpecialtyCareer(career: string, lang: 'fr' | 'ar', id?: string): string {
  if (lang === 'fr' || !career) return career;
  if (id && SPECIALTIES_TRANSLATIONS[id] && SPECIALTIES_TRANSLATIONS[id].career) {
    return SPECIALTIES_TRANSLATIONS[id].career!;
  }
  return career;
}

export function translateInstituteName(name: string, lang: 'fr' | 'ar'): string {
  if (lang === 'fr' || !name) return name;

  // Standard format is "INFSPM WilayaName"
  const upperName = name.toUpperCase();
  if (upperName.includes("INFSPM") || upperName.includes("INSTITUT")) {
    // Extract the wilaya name part
    const parts = name.split(/\s+/);
    const wilayaPart = parts[parts.length - 1]; // usually the last word
    const arWilaya = translateWilaya(wilayaPart, 'ar');
    return `المعهد الوطني للتكوين العالي لشبه الطبي ${arWilaya}`;
  }
  return name;
}

export function translateStudyDuration(duration: string, lang: 'fr' | 'ar'): string {
  if (lang === 'fr' || !duration) return duration;
  if (duration.includes("3 ans")) return "3 سنوات";
  if (duration.includes("3 à 4 ans")) return "3 إلى 4 سنوات";
  return duration;
}

export function translateSemesterName(semName: string, lang: 'fr' | 'ar'): string {
  if (lang === 'fr' || !semName) return semName;
  const num = semName.replace(/\D/g, "");
  if (num) return `السداسي ${num}`;
  return semName;
}
